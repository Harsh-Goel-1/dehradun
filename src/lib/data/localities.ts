export function getLocalityBySlug(slug: string): Locality | undefined {
  return LOCALITIES.find((l) => l.slug === slug);
}

export function getLocalityNameFromSlug(slug: string): string {
  const locality = getLocalityBySlug(slug);
  return locality?.name || slug.replace(/(property-in-|flats-in-|plots-in-|properties-in-)/g, '').replace(/-/g, ' ');
}

export function getAllLocalitySlugs(): string[] {
  return LOCALITIES.map((l) => l.slug);
}

export function getAllLocalityNames(): string[] {
  return [...new Set(LOCALITIES.map((l) => l.name))];
}
